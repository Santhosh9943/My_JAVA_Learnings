package constructorDoc;
/*

A) Important Notes:

1.	Constructors are invoked implicitly when you instantiate objects.

2.	In JAVA, Every constructor that is invoked automatically when an object of the class is created.
	A constructor is a similar to a method but it actual, it is not a method.
	
3.	The constructor is called when a object of a class is created.
	It can be used to set initial values for object attributes.
	
------------------------------------------------------------------------------------------------------

B) The two(2) rules to creating a constructor are: 

Rule 1: Constructor must be same as name of the class.
Rule 2: A Java constructor must not have a return type.

	>> If a class doesn't have a constructor, 
	   The Java compiler automatically creates a default constructor during run-time.
	>> The default constructor initializes instance variable with default values.
	   For example, The int variable will be initialized to 0.
	  
------------------------------------------------------------------------------------------------------

C) Types of constructors:

	>> In Java, Constructors can be Divided into 3 types:
	
		1.No-Argument Constructor (or)
		2.Default Constructor (and)
		3.Parameterized Constructor.


*/

